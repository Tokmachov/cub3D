#include "../cub3d.h"
#include "../libft/libft.h"

t_bool	is_player(char c)
{
	char *player;

	player = ft_strrchr(PLAYER_POSITION_MAP_ELEMENTS, c);
	if (player)
		return (true);
	else
		return (false);
}
